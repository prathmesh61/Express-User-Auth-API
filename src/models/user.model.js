const mongoose = require("mongoose");

// Define User Schema
const userSchema = new mongoose.Schema(
  {
    username: {
      type: String,
      require: true,
    },
    email: {
      type: String,
      require: true,
    },
    password: {
      type: String,
      require: true,
    },
  },
  { timestamps: true }
);

// User model
export const User = mongoose.model("User", userSchema);
